    private void btn_resultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_resultadoActionPerformed
        
        valor2 = ta_resultado.getText();
        
        boolean efloat = (valor1.contains("."));
        boolean efloat2 = (valor2.contains("."));
        
        ta_resultado.setText("");
        
        switch (opcao){
        
            case 1 -> {
                
                if(efloat || efloat2){
                    
                    resultado = calc.soma(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultado));
                    
                }else{
                
                    resultadoint = (int) calc.soma(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultadoint));
                    
                }
                
                
            }
                
            case 2 -> {
                
                if(efloat || efloat2){
                    
                    resultado = calc.subtracao(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultado));
                    
                }else{
                
                    resultadoint = (int) calc.subtracao(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultadoint));
                    
                }
                
            }
                
            case 3 -> {
                
                if(efloat || efloat2){
                    
                    resultado = calc.divisao(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultado));
                    
                }else{
                
                    resultadoint = (int) calc.divisao(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultadoint));
                    
                }

            }
            
            case 4 -> {
                
                if(efloat || efloat2){
                    
                    resultado = calc.multiplicacao(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultado));
                    
                }else{
                
                    resultadoint = (int) calc.multiplicacao(valor1, valor2);
                    ta_resultado.setText(String.valueOf(resultadoint));
                    
                }

            }
                
            default -> throw new AssertionError();
        
        }
        
    }//GEN-LAST:event_btn_resultadoActionPerformed

